let appendData = (data) => {
    let cont = document.getElementById("container");
    cont.innerHTML = null;
    data.forEach(({ image1, image2, brand, title, str_price, price, discount }) => {
        let main_div = document.createElement('div');
        let div1 = document.createElement('div');
        let div2 = document.createElement('div');
        let div3 = document.createElement('div');
        let div4 = document.createElement('div');

        let imag = document.createElement('img')
        imag.src = image1;

        div1.append(imag);
        div1.addEventListener("click",function(){
            window.location.href="product.html"
        })

        let br = document.createElement('h4');
        br.innerText = brand;
        br.style.color = "#7c768a"

        let tl = document.createElement('h4');
        tl.innerText = title;

        div2.append(br, tl);

        let pr = document.createElement('p');
        pr.innerText = price;

        let str = document.createElement('p');
        str.innerText = str_price;

        div3.append(pr, str);

        let dis = document.createElement('p');
        dis.innerText = discount;
        dis.style.color = "#ff6e6e"

        div4.append(dis);

        main_div.append(div1, div2, div3, div4);

        let obj = {
            image1, image2, brand, title, str_price, price, discount
        }

        main_div.addEventListener("click" ,function() {
            sendData(obj);
        })

        cont.append(main_div)
    })
}

